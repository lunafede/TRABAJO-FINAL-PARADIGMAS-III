     private void ComboCliente(){
        ControladorCliente cc = new ControladorCliente();
        ArrayList<Cliente> alcli = new ArrayList<>();
        
        alcli = cc.MostrarTodo();
       
        Cliente[] obj= new Cliente[alcli.size()];
        for (int i = 0; i < alcli.size(); i++) {
            obj[i]= alcli.get(i);
          
        }
        DefaultComboBoxModel<Cliente> boxModel= new DefaultComboBoxModel<>(obj);
        jcCliente.setModel(boxModel);
        jcCliente.setSelectedItem(null);
        
    }
     
