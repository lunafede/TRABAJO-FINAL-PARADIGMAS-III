    private Factura formularioFactura (){
        
        Factura factu = new Factura();
        factu.setNumero(Integer.parseInt(jtNumero.getText()));
        factu.setFecha(FechaSQL.convertirfechasql(jDFecha.getDate()));
        factu.setTotal(Integer.parseInt(jtotal.getText()));
        factu.setFormasDePago((String) jcFormasdePago.getSelectedItem());
        factu.setCliente((Cliente)jcCliente.getSelectedItem());
        factu.setCantidadDeProd(Integer.parseInt(jtCant.getText()));
        factu.setDetalle(this.alDetaPer);
         return factu;
    }
    
    
    
    
    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        // TODO add your handling code here:
        
          Producto p = new Producto();
          Renglon d = new Renglon();
          
          p =(Producto) JcProducto.getSelectedItem();
            
            precio=p.getPrecio();
            cant=Integer.parseInt(jtCant.getText());
            
            d.setCantidad(cant);
            d.setPrecioUni(precio);
            
            subtotal=precio*cant;
            d.setSubTotal(subtotal);
            d.setProducto(p);
            alDetaPer.add(d);
            
            JtablaProducto.setModel(new GrillaRenglonFact(alDetaPer));
           
            total=subtotal+auxSub;
            
            auxSub=total;
            
            jtotal.setText(String.valueOf(total));
          
    }//GEN-LAST:event_jbAgregarActionPerformed

